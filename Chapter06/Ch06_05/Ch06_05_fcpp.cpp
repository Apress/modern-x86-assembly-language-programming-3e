#include "Ch06_05.h"

int64_t MulIntegers_cpp(int8_t a, int16_t b, int32_t c, int64_t d,
    int8_t e, int16_t f, int32_t g, int64_t h)
{
    return a * b * c * d * e * f * g * h;
}
